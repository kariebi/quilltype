test("loads config, validates OpenAPI, and generates TypeScript output", async () => {
  const tempDir = await mkdtemp(path.join(os.tmpdir(), "typebridge-"));

  try {
    const fixtureSource = path.resolve("examples/petstore.openapi.json");
    const configPath = path.join(tempDir, "typebridge.config.json");

    await writeFile(
      configPath,
      JSON.stringify(
        {
          source: {
            path: fixtureSource,
          },
          outputs: [
            {
              path: "./generated/petstore.ts",
              mode: "types",
            },
          ],
          validation: {
            requireOperationIds: true,
            requireResponseSchemas: true,
          },
        },
        null,
        2,
      ),
      "utf8",
    );

    const { config } = await loadConfig(configPath);
    const loaded = await loadOpenApiDocument(config, configPath);

    validateOpenApiDocument(loaded.document, config);

    const [result] = await generateOutputs({
      config,
      configPath,
      document: loaded.document,
      sourceLabel: loaded.sourceLabel,
    });

    assert.equal(result.changed, true);

    const generatedContent = await readFile(result.outputPath, "utf8");
    assert.match(generatedContent, /This file was generated by TypeBridge\./);
    assert.match(generatedContent, /Output mode: types/);
    assert.match(generatedContent, /export (interface|type) paths/);
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
});

